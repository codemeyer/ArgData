    private static List<byte> CreateRange(int from, int to)
    {
        var range = new List<byte>();

        for (int i = from; i <= to; i++)
        {
            range.Add((byte)i);
        }

        return range;
    }

    private static List<byte> CreateReverseRange(int from, int to)
    {
        var range = new List<byte>();

        for (int i = to; i >= from; i--)
        {
            range.Add((byte)i);
        }

        return range;
    }
